public class TipeDataString {
    /*
    tipe data string adalah kumpulan dari karakter
    direpresentasikan dengan keyword String
    defaultnya null
     */
    public static void main(String[] args) {
        String hellow = "hellow",
                world = "world",
                helloWorld = "ini adalah isi variable hellow dan world "+hellow + " "+world;
        System.out.println(helloWorld);
    }
}
